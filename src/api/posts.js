export const getPosts = async function (page = 1, limit = 50) {
  console.log("API-posts -> getPosts START")
  let array = [];

  await fetch('http://localhost:5000/posts').then(response => response.json().then(result => posts = result)).catch(err => "Error Fetching all post: " + err)
  console.log("Total Posts: ",posts.length," Pages: ", page," Limit: ", limit)
  for (let index = (limit * (page - 1)); index < (page * limit) && posts[index]; index++) { array.push(posts[index]); }
  console.log("API-posts -> getPosts END")
  return array
}

